#include "3-calc.h"

/**
 * op_add - func that adds 2 int
 * @a: first integer
 * @b: second integer
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - func that sub 2 int
 * @a: first integer
 * @b: second integer
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - func that mul 2 int
 * @a: first integer
 * @b: second integer
 *
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - func that div 2 int
 * @a: first integer
 * @b: second integer
 *
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - func that mod 2 int
 * @a: first integer
 * @b: second integer
 *
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
